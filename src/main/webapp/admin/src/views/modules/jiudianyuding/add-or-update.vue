<script>
import { 
	isNumber,
	isIntNumer,
} from "@/utils/validate";
export default {
	data() {
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				jiudianmingcheng : false,
				jiudiantupian : false,
				kefangleixing : false,
				kefangjiage : false,
				kefangshuliang : false,
				zongfeiyong : false,
				yudingshijian : false,
				zhanghao : false,
				xingming : false,
				yudingbeizhu : false,
				ispay : false,
			},
			
			
			ruleForm: {
				jiudianmingcheng: '',
				jiudiantupian: '',
				kefangleixing: '',
				kefangjiage: '',
				kefangshuliang: '',
				zongfeiyong: '',
				yudingshijian: '',
				zhanghao: '',
				xingming: '',
				yudingbeizhu: '',
			},
		

			
			rules: {
				jiudianmingcheng: [
				],
				jiudiantupian: [
				],
				kefangleixing: [
				],
				kefangjiage: [
					{ validator: validateNumber, trigger: 'blur' },
				],
				kefangshuliang: [
					{ required: true, message: '预定数量不能为空', trigger: 'blur' },
					{ validator: validateIntNumber, trigger: 'blur' },
				],
				zongfeiyong: [
					{ validator: validateNumber, trigger: 'blur' },
				],
				yudingshijian: [
				],
				zhanghao: [
				],
				xingming: [
				],
				yudingbeizhu: [
					{ required: true, message: '预定备注不能为空', trigger: 'blur' },
				],
				ispay: [
				],
			},
		};
	},
	props: ["parent"],
	computed: {


		zongfeiyong:{
			get: function () {
				return 1*this.ruleForm.kefangjiage*this.ruleForm.kefangshuliang
			}
		},

	},
    components: {
    },
	created() {
		this.ruleForm.yudingshijian = this.getCurDate()
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='jiudianmingcheng'){
							this.ruleForm.jiudianmingcheng = obj[o];
							this.ro.jiudianmingcheng = true;
							continue;
						}
						if(o=='jiudiantupian'){
							this.ruleForm.jiudiantupian = obj[o];
							this.ro.jiudiantupian = true;
							continue;
						}
						if(o=='kefangleixing'){
							this.ruleForm.kefangleixing = obj[o];
							this.ro.kefangleixing = true;
							continue;
						}
						if(o=='kefangjiage'){
							this.ruleForm.kefangjiage = obj[o];
							this.ro.kefangjiage = true;
							continue;
						}
						if(o=='kefangshuliang'){
							this.ruleForm.kefangshuliang = obj[o];
							this.ro.kefangshuliang = true;
							continue;
						}
						if(o=='zongfeiyong'){
							this.ruleForm.zongfeiyong = obj[o];
							this.ro.zongfeiyong = true;
							continue;
						}
						if(o=='yudingshijian'){
							this.ruleForm.yudingshijian = obj[o];
							this.ro.yudingshijian = true;
							continue;
						}
						if(o=='zhanghao'){
							this.ruleForm.zhanghao = obj[o];
							this.ro.zhanghao = true;
							continue;
						}
						if(o=='xingming'){
							this.ruleForm.xingming = obj[o];
							this.ro.xingming = true;
							continue;
						}
						if(o=='yudingbeizhu'){
							this.ruleForm.yudingbeizhu = obj[o];
							this.ro.yudingbeizhu = true;
							continue;
						}
				}
				this.ruleForm.kefangshuliang = 0
				this.ro.kefangshuliang = false;
			}
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					var json = data.data;
					if(this.$storage.get("role")!="管理员") {
						this.ro.kefangjiage = true;
					}
					if(((json.zhanghao!=''&&json.zhanghao) || json.zhanghao==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.zhanghao = json.zhanghao
						this.ro.zhanghao = true;
					}
					if(((json.xingming!=''&&json.xingming) || json.xingming==0) && this.$storage.get("role")!="管理员"){
						this.ruleForm.xingming = json.xingming
						this.ro.xingming = true;
