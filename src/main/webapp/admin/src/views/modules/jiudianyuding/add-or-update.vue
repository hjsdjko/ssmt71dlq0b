    // 提交
    onSubmit() {
		this.ruleForm.ispay = '未支付'
        this.ruleForm.zongfeiyong = this.zongfeiyong
	if(this.ruleForm.jiudiantupian!=null) {
		this.ruleForm.jiudiantupian = this.ruleForm.jiudiantupian.replace(new RegExp(this.$base.url,"g"),"");
